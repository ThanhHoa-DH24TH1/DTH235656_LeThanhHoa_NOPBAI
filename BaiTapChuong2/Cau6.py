# Ý nghĩa các toán tử trong Python:

# /  : Chia lấy kết quả thực (float division)
#      Ví dụ: 7 / 2 = 3.5

# // : Chia lấy phần nguyên (floor division)
#      Ví dụ: 7 // 2 = 3

# %  : Chia lấy phần dư (modulo)
#      Ví dụ: 7 % 2 = 1

# ** : Lũy thừa (power)
#      Ví dụ: 2 ** 3 = 8

# and: Toán tử logic "và"
#      Kết quả True nếu cả hai vế đều True
#      Ví dụ: True and False = False

# or : Toán tử logic "hoặc"
#      Kết quả True nếu ít nhất một vế là True
#      Ví dụ: True or False = True

# is : So sánh hai đối tượng có cùng tham chiếu không (identity)
#      Ví dụ: a is b (True nếu a và b cùng trỏ đến một đối tượng)